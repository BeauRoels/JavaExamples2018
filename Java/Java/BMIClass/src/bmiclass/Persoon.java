package bmiclass;

public class Persoon 
{
    private int bmi;
    private int massa;
    private int lengte;

    
    public int getBmi() {
        return bmi;
    }
    public void setBmi(int bmi) {
        this.bmi = bmi;
    }

    public int getMassa() {
        return massa;
    }

    public void setMassa(int massa) {
        this.massa = massa;
    }

    public int getLengte() {
        return lengte;
    }

    public void setLengte(int lengte) {
        this.lengte = lengte;
    }
}
